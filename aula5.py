# Tipo de dado bool (boolean)
# Ao questionar algo em um programa só existem duas repostar possíveis :
# Sim (True) ou Não (False).
# Existemm vários operadores para "questionar" dentre eles o ==, 
# Que é um operador lógico que questionar se um valor é igual a outro.

print(10 == 10) # Sim -> True (Verdadeiro)
print(10 == 100) # Não -> False (Falso)
print(type(10 == 10))
print(type(10 == 100))
print(type(True))
print(type(False))